//   print out num 10 to 0 and then 0 to 10
class Main{
    public static void main (String[] args){
        // control flow for loop
        
        for (int  i =10; i>=1;i--){
            System.out.println("The  the value of i  : " +i);
        }
        for (int  i =1; i<=10;i++){
            System.out.println("The  the value of i  : " +i);
        }
    }
}
